"use client";

import { useEffect } from "react";
import AOS from "aos";
import "aos/dist/aos.css";

export default function AOSProvider() {
  useEffect(() => {
    AOS.init({
      duration: 1100,
      once: true,
      easing: "ease-out-cubic",
      offset: 80,
    });
  }, []);

  return null;
}